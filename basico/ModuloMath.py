#!/usr/bin/env python3
# -*- coding: utf-8 -*
import math


class ModuloMath():
	'''
	Métodos que utilizam lambda, para programação funcional
	'''
	def __init__(self):
		pass

	def menor(self):
		return math.ceil(3.4)

	def absoluto(self):
		return math.fabs(-4)

	def piso(self):
		return math.floor(3.7)

	def euler(self):
		return math.exp(3)

	def log10(self):
		return math.log10(100)

	def potencia(self):
		return math.pow(2,10)

	def raiz(self):
		return math.sqrt(16)